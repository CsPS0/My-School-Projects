csillag:int = int(input('Ird be, hogy hány 1-5db csillagot kapjon Béla: '))

if csillag == 5:
    print('Béla egy kibaszott okos tanuló! Összesen 5 csillagot ért el!')
elif csillag == 4:
    print('Béla majdnem egy kibaszott okos tanuló! Összesen 4 csillagot ért el!')
elif csillag == 3:
    print('Béla már nem annyira kibaszott okos tanuló! Összesen 3 csillagot ért el!')
elif csillag == 2:
    print('Béla szinte semenyire nem egy kibaszott okos tanuló! Összesen 2 csillagot ért el!')
elif csillag == 1:
    print('Béla nem egy okos tanuló! Összesen 1 csillagot ért el!')